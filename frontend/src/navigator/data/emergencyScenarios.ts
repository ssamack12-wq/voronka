const RISK_LABELS: Record<EmergencyRiskLevel, string> = {
  low: 'Низкий',
  medium: 'Средний',
  high: 'Высокий'
};

export function emergencyRiskLabel(level: EmergencyRiskLevel): string {
  return RISK_LABELS[level];
}

export function searchEmergencyScenarios(query: string): EmergencyScenario[] {
  const q = query.trim().toLowerCase();
  if (!q) return EMERGENCY_SCENARIOS;

  return EMERGENCY_SCENARIOS.filter((s) => {
    const haystack = [
      s.title,
      s.category,
      s.problem,
      s.specialistWhen,
      ...s.actionsNow,
      ...s.dontDo
    ]
      .join(' ')
      .toLowerCase();
    return haystack.includes(q);
  });
}
