/** Для земельных сценариев дополняем базовые подзадачи шага */
export function mergeLandSubtasks(
  stepId: string,
  baseSubtasks: TutorialSubtask[],
  scenarioId?: string
): TutorialSubtask[] {
  if (!scenarioId || !LAND_SCENARIO_IDS.includes(scenarioId as (typeof LAND_SCENARIO_IDS)[number])) {
    return baseSubtasks;
  }
  const landExtra = LAND_STEP_SUBTASKS[stepId];
  if (!landExtra) return baseSubtasks;
  return [...baseSubtasks, ...landExtra];
}
