function nbTut(stepId: string, extra: Partial<Tutorial> = {}): Tutorial {
  const step = NEWBUILDING_STEPS[stepId];
  const base = buildFallbackTutorial(step, `tut-${stepId}`);
  return { ...base, ...extra, id: `tut-${stepId}`, stepId };
}

