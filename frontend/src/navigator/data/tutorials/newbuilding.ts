/** Fill tutorials for all newbuilding steps */
export function getAllNewbuildingTutorials(): Record<string, Tutorial> {
  const out = { ...NEWBUILDING_TUTORIALS };
  for (const stepId of Object.keys(NEWBUILDING_STEPS)) {
    const tutId = `tut-${stepId}`;
    if (!out[tutId]) {
      out[tutId] = nbTut(stepId);
    }
  }
  return out;
}
